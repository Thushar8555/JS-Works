
// add 

let add=(n1,n2)=>n1+n2
console.log(add(10,20));

// sub

let sub=(n1,n2)=>n1-n2
console.log(sub(20,10));

// last digit max

let lastmaxdigit=(n1,n2)=> n1%10>n2%10?n1:n2
console.log(lastmaxdigit(148,135));


// last digit sum

let lastdigitsum=(n1,n2)=>n1%10+n2%10
console.log(lastdigitsum(120,123));


// square 

let square=(n1)=>n1**2
console.log(square(3));


// maxtwo

let maxtwo=(n1,n2)=>n1>n2?n1:n2
console.log(maxtwo(10,20));


